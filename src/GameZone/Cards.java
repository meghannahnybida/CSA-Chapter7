package GameZone;

public class Cards {

    private String suit;
    private String value;

    public String getSuit()
    {
        return suit;
    }
    public void setSuit(String s)
    {
        suit = s;
    }
    public String getValue()
    {
        return value;
    }
    public void setValue(String v)
    {
        value = v;
    }

}
