package GameZone;

public class Cards {

    private String suit;
    private int value;

    public String getSuit()
    {
        return suit;
    }
    public void setSuit(String s)
    {
        suit = s;
    }
    public int getValue()
    {
        return value;
    }
    public void setValue(int v)
    {
        value = v;
    }

}
